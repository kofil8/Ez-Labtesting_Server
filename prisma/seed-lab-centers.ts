async function seedLabCenters() {
  console.log('🌱 Seeding lab centers...');

  try {
    // Clear existing lab centers
    await prisma.labCenter.deleteMany({});
    console.log('✅ Cleared existing lab centers');

    // Create new lab centers
    const created = await prisma.labCenter.createMany({
      data: labCentersData,
      skipDuplicates: true,
    });

    console.log(`✅ Created ${created.count} lab centers`);
    console.log('🎉 Lab centers seeded successfully!');
  } catch (error) {
    console.error('❌ Error seeding lab centers:', error);
    throw error;
  }
}

// Run if called directly
if (require.main === module) {
  seedLabCenters()
    .catch((error) => {
      console.error(error);
      process.exit(1);
    })
    .finally(async () => {
      await prisma.$disconnect();
    });
}

export { seedLabCenters };
